"""
    File: tuple.py
    About: A tuple resembles a list, but is immutable
    About: Indicated by enclosing its elements in ()
"""

fruits = ("apple", "banana")
print("fruits")
print(fruits)

meats = ("fish", "poultry")
print("meats")
print(meats)

food = meats + fruits
print("food")
print(food)

veggies = ["celery", "beans"]
print("tuple(veggies)")
print(tuple(veggies))