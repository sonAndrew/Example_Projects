"""
    File: remove.py
    About: Removing Elements From a List
"""
example = [1, 2, 3, 11, 12, 13, 14, 15]

# Permanently Removes the last element
example.pop()
print(example)
print("-")

# Permanently Removes the 6th element
example.pop(6)
print(example)
print("-")

# Permanently Removes the 5th element
example.pop(5)
print(example)
print("-")

# Permanently Removes the 1st element
example.pop(0)
print(example)
print("-")

